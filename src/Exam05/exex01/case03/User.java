package Exam05.exex01.case03;

public class User {
	private String userName;
	private int age;
	
	public void setUserName(String userName) {
		this.userName = userName;
	}
	
	public void setAge(int age) {
		this.age = age;
	}
	
	public String getUserName() {
		return this.userName;
	}
	
	public int getAge() {
		return this.age;
	}
}
